function scoreCase(testCase) {
  const predicted = core.classify(testCase.text);
  const intentCorrect = predicted.intent === testCase.intent;
  const categoryCorrect = predicted.slots.category === testCase.category;
  const colorCorrect = predicted.slots.color === testCase.color;
  return { ...testCase, predicted, intentCorrect, categoryCorrect, colorCorrect };
}

const evaluated = cases.map(scoreCase);
const intentCorrect = evaluated.filter((item) => item.intentCorrect).length;
const categoryRelevant = evaluated.filter((item) => item.category !== null).length;
const categoryCorrect = evaluated.filter((item) => item.category !== null && item.categoryCorrect).length;
const colorRelevant = evaluated.filter((item) => item.color !== null).length;
const colorCorrect = evaluated.filter((item) => item.color !== null && item.colorCorrect).length;

const metrics = {
  totalCases: cases.length,
  intentAccuracy: intentCorrect / cases.length,
  categorySlotAccuracy: categoryCorrect / categoryRelevant,
  colorSlotAccuracy: colorCorrect / colorRelevant,
  exactMatchAccuracy: evaluated.filter((item) => item.intentCorrect && item.categoryCorrect && item.colorCorrect).length / cases.length
};

const outDir = path.join(__dirname, "..", "paper", "figures");
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(path.join(outDir, "evaluation_results.json"), JSON.stringify({ metrics, evaluated }, null, 2));

